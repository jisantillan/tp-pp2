package org.domingus.config;

public class DataFetcherConfiguration {

    private String name;
    private String url;
    private String date;

    public DataFetcherConfiguration() {
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public String getDate() {
        return date;
    }

}
